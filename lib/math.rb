def addition(num1, num2)
  sum = num2 + num1
  puts sum
end

def subtraction(num1, num2)
  difference = num2 - num1
  puts difference
end

def division(num1, num2)
  dividend = num2 / num1
  puts dividend
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
end

def modulo(num1, num2)
  puts num1 % num2
end

def square_root(num)
  puts Math.sqrt(num)
end